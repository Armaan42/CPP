#include<iostream>
using namespace std;

int main(){
	char num;
	cin>>num;

	switch(num){
		case '+':
			cout<<"first"<<endl;
			break;
		case '-':
			cout<<"second"<<endl;
			break;
		case '*':
			cout<<"third"<<endl;
			break;
		case'/':
			cout<<"fourth"<<endl;
			break;
	}
	//cout<<"good job"<<endl;
}

